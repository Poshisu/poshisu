#!/usr/bin/env node
import { readFileSync, writeFileSync } from 'node:fs';
import { spawnSync } from 'node:child_process';

const mode = process.argv[2] ?? 'write';

function runSupabase(args) {
  return spawnSync('pnpm', args, { encoding: 'utf8' });
}

let generated = spawnSync('supabase', ['gen', 'types', 'typescript', '--local'], { encoding: 'utf8' });

if (generated.status !== 0) {
  generated = runSupabase(['exec', 'supabase', 'gen', 'types', 'typescript', '--local']);
}

if (generated.status !== 0) {
  const fallback = runSupabase(['dlx', 'supabase', 'gen', 'types', 'typescript', '--local']);
  if (fallback.status === 0) {
    generated = fallback;
  } else {
    process.stderr.write(
      [
        generated.stderr?.trim() || fallback.stderr?.trim() || 'Failed to generate Supabase types.',
        'Tried `supabase ...`, `pnpm exec supabase ...`, and `pnpm dlx supabase ...`.',
        'Tip: confirm network egress + run `pnpm exec supabase --version` locally.',
      ].join('\n') + '\n',
    );
    process.exit(generated.status ?? fallback.status ?? 1);
  }
}

const header = [
  '// Generated by: pnpm db:types',
  '// Run `pnpm db:types` to regenerate from your local Supabase instance.',
  '',
].join('\n');

const nextOutput = `${header}${generated.stdout}`.replace(/\s+$/u, '\n');

if (mode === 'stdout') {
  process.stdout.write(nextOutput);
  process.exit(0);
}

const targetFile = 'src/types/database.ts';
const current = readFileSync(targetFile, 'utf8');

if (mode === 'check') {
  if (current !== nextOutput) {
    writeFileSync('/tmp/poshisu-generated-database.ts', nextOutput);
    const diff = spawnSync(
      'git',
      ['--no-pager', 'diff', '--no-index', '--', targetFile, '/tmp/poshisu-generated-database.ts'],
      { encoding: 'utf8' },
    );
    process.stderr.write(
      [
        'Generated database types are stale.',
        'Run `pnpm db:types` and commit the updated src/types/database.ts file.',
        'Diff against generated output:',
        (diff.stdout || diff.stderr || '').slice(0, 60000),
      ].join('\n') + '\n',
    );
    process.exit(1);
  }

  process.stdout.write('Database types are up to date.\n');
  process.exit(0);
}

process.stdout.write(nextOutput);
